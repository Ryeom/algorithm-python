def max_saw_height(trees, m):
    left = 0
    right = max(trees)
    answer = 0

    def is_ok(trees, m, h):
        total = 0
        for tree in trees:
            if tree > h:
                total += (tree - h)

        return total >= m

    while left <= right:
        mid = (left + right) // 2
        if is_ok(trees, m, mid):
            answer = mid
            left = mid + 1
        else:
            right = mid - 1

    return answer  # ìž„ì‹œê°’


